def generate_rejected_for_sample(index: int, instruction: str, chosen: str) -> str:
    """
    为样本生成 rejected 回答
    index < 200: 使用预定义的回答
    index >= 200: 用简单策略生成
    """
    if index < len(REJECTED_200):
        return REJECTED_200[index]

    # 200 条之后用简单策略生成 rejected
    # 策略 1: 只提取答案部分，去掉推理过程
    if "所以答案是" in chosen:
        conclusion = chosen.split("所以答案是")[-1].strip()
        if len(conclusion) < len(chosen) // 3:
            return conclusion + "。"

    # 策略 2: 取第一句话
    if "。" in chosen:
        first_sentence = chosen.split("。")[0]
        if len(first_sentence) < len(chosen) // 3 and len(first_sentence) > 5:
            return first_sentence + "。"

    # 策略 3: 对于短答案，直接给一个模糊回答
    if len(chosen) < 60:
        return "这个嘛，不太好说。"

    # 策略 4: 截断（确保不超过 50%）
    max_len = len(chosen) // 3
    if max_len > 20:
        return chosen[:20] + "..."

    return "这个我不太确定。"


def convert_sft_to_dpo(sft_data: list) -> list:
    dpo_data = []

    for i, item in enumerate(sft_data):
        instruction = item.get("instruction", "")
        input_text = item.get("input", "")
        chosen = item.get("output", "")

        rejected = generate_rejected_for_sample(i, instruction, chosen)

        dpo_item = {
            "instruction": instruction,
            "input": input_text,
            "chosen": chosen,
            "rejected": rejected,
        }
        dpo_data.append(dpo_item)

        if (i + 1) % 100 == 0:
            print(f"Processed {i + 1}/{len(sft_data)} samples")

    return dpo_data


def main():
    input_path = "./data/sft-1000.jsonl"
    output_path = "./data/dpo/dpo-1000.jsonl"

    print(f"Loading SFT-1000 dataset from {input_path}...")
    sft_data = load_jsonl(input_path)
    print(f"Loaded {len(sft_data)} samples")

    print("\nConverting to DPO format...")
    print("Rejected types: 1) 只给结论 2) 错误答案 3) 不完整解释")
    dpo_data = convert_sft_to_dpo(sft_data)

    print(f"\nSaving DPO-1000 dataset to {output_path}...")
    save_jsonl(dpo_data, output_path)

    print(f"\nDone! Generated {len(dpo_data)} DPO samples")

    print("\n" + "=" * 60)
    print("Sample examples:")
    print("=" * 60)

    for i in [0, 1, 5, 10, 50, 100, 150, 200]:
        if i < len(dpo_data):
            item = dpo_data[i]
            print(f"\n[Sample {i + 1}]")
            print(f"Instruction: {item['instruction']}")
            print(f"Chosen:   {item['chosen'][:60]}...")
            print(f"Rejected: {item['rejected']}")


if __name__ == "__main__":
    main()
